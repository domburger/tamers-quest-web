import { chromium } from "playwright";
const URL = process.env.GAME_URL || "http://localhost:8080";
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
const browser = await chromium.launch({ headless: true, args: ["--use-gl=angle", "--use-angle=swiftshader", "--enable-unsafe-swiftshader", "--ignore-gpu-blocklist"] });
const page = await browser.newPage({ viewport: { width: 1280, height: 720 }, deviceScaleFactor: 2 });
await page.goto(URL, { waitUntil: "networkidle" });
await page.waitForSelector("canvas", { timeout: 15000 });
await sleep(8000);
await page.click("#guestBtn");
await sleep(600);
await page.fill("#guest-nick", "Scout");
await page.click("#guest-go");
await sleep(3000);
await page.screenshot({ path: ".screenshots/diag-charselect.png" });
console.log("shot diag-charselect");
// try the new-character click and screenshot the result
await page.mouse.click(640, 640);
await sleep(1500);
await page.screenshot({ path: ".screenshots/diag-after-newchar.png" });
console.log("inputs:", await page.locator('input').count());
await browser.close();
